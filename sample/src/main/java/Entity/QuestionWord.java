    //Xóa dấu của một kí tự
    public char removecharAccent(char a)
    {
        char k = a;
        //Kiểm tra kí tự đó có thuộc kí tự có dấu không nếu có thì thay kí tự đó bằng kí tự không dấu.
        //Nếu kí tự không có dấu thì dữ nguyên
        for(int i= 0;i<SOURCE_CHARACTERS.length;i++ )
        {
            if(a==SOURCE_CHARACTERS[i])
            {
                k= DESTINATION_CHARACTERS[i];
                break;
            }
        }
        return k;
    }

    //Xóa dấu của kết quả câu hỏi
    public String RemoveAccent()
    {
        String result1 ="";
        for (int i=0;i<result.length();i++)
        {
            //Nếu khác kí tự rỗng thì xóa dấu
            if(result.charAt(i)!=' ')
            {
                result1+=removecharAccent(result.charAt(i));
            }

        }
        return result1;
    }

